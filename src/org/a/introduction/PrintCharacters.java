package org.a.introduction;


/**
 * The type Print characters.
 */
public class PrintCharacters {

    /**
     * The entry point of application.
     *
     * @param args the input arguments
     */
    public static void main(String[] args) {

        for(char c = 'a'; c <= 'z'; c++){
            System.out.print(c +" ");
        }
    }
}


